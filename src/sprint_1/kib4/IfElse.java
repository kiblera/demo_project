package sprint_1.kib4;

// Write a single class with a single main method that demonstrates the use of an if -> else/if -> else ladder.

public class IfElse {

    public static void main(String[] args) {
        int i = 0;

        if (i < 5) {
            System.out.println("something");

        } else if (i < 10) {
            System.out.println("something else");

        } else {
            System.out.println("nothing");
        }
    }
}